        public void Load()
    {
        CAmarksDAOimp dao = new CAmarksDAOimp();
        List< CAmarks> list = dao.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for( CAmarks ca: list)
        {
            String course_id=ca.getCourse_id();
            String stu_id=ca.getStu_id();
            Float q1=ca.getQ1();
            Float q2=ca.getQ2();
            Float q3=ca.getQ3();
            Float q4=ca.getQ4();
            Float ass1=ca.getAss1();
            Float ass2=ca.getAss2();
            Float ass3=ca.getAss3();
            Float mid=ca.getMid();
            
            DFT.addRow(new Object[]{course_id,stu_id,q1,q2,q3,q4,ass1,ass2,ass3,mid});
        }     
  
    }
    
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        CAmarks ca = new CAmarks();
        
        String sid = txtsid.getText();
        String cid = txtcid .getText();
        Float quiz1 = Float.parseFloat(txtq1 .getText());
        Float quiz2 = Float.parseFloat(txtq2 .getText());
        Float quiz3 = Float.parseFloat(txtq3 .getText());
        Float quiz4 = Float.parseFloat(txtq4 .getText());
        Float ass1 = Float.parseFloat(txtass1 .getText());
        Float ass2 = Float.parseFloat(txtass2 .getText());
        Float ass3 = Float.parseFloat(txtass3 .getText());
        Float mid = Float.parseFloat(txtmid .getText());
        
        
        ca.setCourse_id(cid);
        ca.setStu_id(sid);
        ca.setQ1(quiz1);
        ca.setQ2(quiz2);
        ca.setQ3(quiz3);
        ca.setQ4(quiz4);
        ca.setAss1(ass1);
        ca.setAss2(ass2);
        ca.setAss3(ass3);
        ca.setMid(mid);
        

        CAmarksDAOimp dao = new CAmarksDAOimp();
        dao.update(ca);
        Load();
        txtsid.setText("");
        txtcid.setText("");
        txtq1.setText("");
        txtq2.setText("");
        txtq3.setText("");
        txtq4.setText("");
        txtass1.setText("");
        txtass2.setText("");
        txtass3.setText("");
        txtmid.setText("");
        txtsid.requestFocus();
    }//GEN-LAST:event_jButton2ActionPerformed

