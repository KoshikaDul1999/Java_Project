       public void Load()
    {
        studentUpdates st = new studentUpdates();
        
        String stu_id = stID.getText();
        String stu_name = fname .getText();
        String stu_address = address .getText();
        String stu_email = email .getText();
        String stu_b_day = DOB .getText();
        Integer stu_p_no=Integer.parseInt(stupno.getText());
        
        
        st.setStu_id(stu_id);
        st.setStu_name(stu_name);
        st.setStu_b_day(stu_b_day);
        st.setStu_email(stu_email);
        st.setStu_address(stu_address);
        st.setStu_p_no(stu_p_no);
        st.setStu_id(stu_id);
        
        st.update(st);
  
    }
