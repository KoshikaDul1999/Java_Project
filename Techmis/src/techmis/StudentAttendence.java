    public void Load()
    {
         
        StudentAttendenceDAOimp dao = new StudentAttendenceDAOimp();
        List< StudentCourseAttendence> list = dao.list();
        DefaultTableModel DFT = (DefaultTableModel) attendance.getModel();
        DFT.setRowCount(0);
        for( StudentCourseAttendence stuAtt: list)
        {
            String stu_id=stuAtt.getStu_id();
            String course_id=stuAtt.getCourse_id();
            int date=stuAtt.getDate();
            String atype=stuAtt.getAType();
            int status=stuAtt.getStatus();
         
            DFT.addRow(new Object[]{stu_id,course_id,date,atype,status});
        }     
  
    }
    
    
    
